const grid = document.getElementById("gameGrid");
const fragment = document.createDocumentFragment();
const gameElements = [];

games.forEach((game, index) => {
    const div = document.createElement("div");
    div.className = "game" + (index === 0 ? " active" : "");

    div.innerHTML = `
        <div class="cover">
            <img src="${game.image}" alt="${game.title}">
        </div>
        <div class="info">
            <h3 class="title">${game.title}</h3>
            <div class="rating">⭐ ${game.rating}</div>
            <div class="genres">
                ${game.genres.split(",").map(g => `<span class="tag">${g.trim()}</span>`).join("")}
            </div>
            <p class="description">${game.description}</p>
            <div class="screenshots">
                ${game.screenshots.map(s => `<img src="${s}" alt="Screenshot">`).join("")}
            </div>
            <button class="discover-btn">Ver más</button>
        </div>
    `;

    div.addEventListener("mouseenter", () => {
        gameElements.forEach(g => g.classList.remove("active"));
        div.classList.add("active");
    });

    fragment.appendChild(div);
    gameElements.push(div);
});

grid.appendChild(fragment);

// Volver al primero al salir
grid.addEventListener("mouseleave", () => {
    gameElements.forEach(g => g.classList.remove("active"));
    if (gameElements[0]) gameElements[0].classList.add("active");
});
